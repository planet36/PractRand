/*

Currently the only non-uniform distribution supported is the Gaussian distribution.  
I've written algorithms for transforming raw random bits to gaussian based upon:
	1. summing small integers (via popcount and/or bit twiddling)
		does not work well - it takes more than you'd expect to produce decent distribution of common events, and a shockingly large number to produce decent distributions of rare events
	2. CDF-based lookup tables, multiple samples, 1st order fit on one sample
		works great for common events, but isn't good at producing rare results (max possible is ~5.9 deviations)
	3. PDF-based "ziggurat"-style lookup
		a bit slower than #2, but it handles rare events well and uses smaller tables too
	4. non-linear indexed CDF-based lookup tables, with 2nd or 3rd order fit within each bucket
		a bit slower than #2, but it handles rare events well, and uses the smallest tables yet (1st order fit just isn't good enough, 2nd order is decent, but 3rd is significantly better; I haven't tried 4th, but don't expect it to help that much)

	...and various hybrids of the above and others.  
	The general take-home is that summing is really bad at producing rare events properly.  
		But decent at correcting subtle imperfections other than lack of rare events.  

	to try next:
		tripple-lookup version of generate_gaussian_popcount_lookup_linear() and generate_gaussian_popcount_lookup_quadratic()
		fixed point ziggurat path
		avx-based micro-lookup

*/


