				std::printf("%g deviation (%s)\n", area_deviation, area_deviation < 1e-15 ? "near-perfect" : area_deviation < 1e-13 ? "very good" : area_deviation < 1e-9 ? "good" : area_deviation < 1e-7 ? "mediocre" : area_deviation < 1e-4 ? "poor" : area_deviation < 0.01 ? "BAD" : area_deviation < 0.1 ? "VERY BAD" : "COMPLETE FAILURE");
				std::printf("refinement took %ld steps (gives up at 1B)\n", refinement_steps);
				std::printf("FP_type stripe_width[%d] = {", NUM_STRIPES+1);
				for (int i = 0; i <= NUM_STRIPES; i++) std::printf("%s%18.16g%s", i % 4 == 0 ? "\n\t" : "", stripe_width[i], i != NUM_STRIPES - 0 ? ", " : "");
