		bool _generate_gaussian_ziggurat(double &result, Uint64 raw64_a, Uint64 raw64_b) {
			/*
				there are a lot of variations on the ziggurat algorithm
				This particular version changes two things compared to the classic:
					the interface accepts a fixed amount of PRNG output, and returns failure if that's not enough
					fast regions within each stripe are trapezoidal
				I'm not quite sure on these two variations, still experimenting
			*/
			using namespace ZigguratStuff;
			using namespace PractRand::Tests;
#if defined GENERATE_ZIGGURAT_STUFF
			if (!inited) init_stripes();
#endif
			int stripe = raw64_a & (NUM_STRIPES - 1);
			double x, ax;
			if (SIGNED_IF_CONVERSION) { // two slightly different ways to do roughly the same thing
				// this one is significantly faster for me atm
				// (it also yields slightly better resolution)
				x = (static_cast<Sint64>(raw64_a) >> 10) * 1.11022302462515654e-16;// 1/0/(1ull << 53)
				x *= stripe_width[stripe];
				ax = std::fabs(x);
			}
			else {
				ax = PractRand::Internals::rand_double_implementation(raw64_a) * stripe_width[stripe];
				x = (raw64_b & 1) ? ax : -ax;
			}
			// there should actually be a third mode there, using fixed point math, but I don't feel like dealing with the intrinsics for that atm
			double threshold = stripe_width[stripe + 1];
#if defined ENABLE_TRAPEZOIDISM
			double y = PractRand::Internals::rand_double_implementation(raw64_b);
			threshold += stripe_extra_width[stripe] * y;
#endif
			if (ax < threshold) {
#if defined ENABLE_TRAPEZOIDISM && defined ERROR_CHECK_TRAPEZOIDISM
				// slow error-checking, disable when trapezoidism is working properly
				if (stripe) {
					y = stripe_y[stripe] - y * (stripe_y[stripe] - stripe_y[stripe - 1]);
					double p = math_normaldist_pdf(x);
					if (p < y) {
						issue_error("ziggurat failed, probably trapezoidism");
					}
				}
#endif
				result = x;
				return true;
			}
#if !defined ENABLE_TRAPEZOIDISM
			double y = PractRand::Internals::rand_double_implementation(raw64_b);
#endif
			if (stripe) {
				y = stripe_y[stripe] - y * (stripe_y[stripe] - stripe_y[stripe - 1]);
				if (math_normaldist_pdf(x) < y) return false;
				result = x;
				return true;
			}
			else {
				y *= tail_area;
				ax = math_pvalue_to_normaldist(y);
				result = (raw64_b & 1) ? ax : -ax;
				return true;
			}
		}
